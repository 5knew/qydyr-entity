-- Показать статистику
SELECT 'Пользователи' as entity, COUNT(*) as count FROM user
UNION ALL
SELECT 'Места', COUNT(*) FROM place
UNION ALL
SELECT 'Афиши', COUNT(*) FROM afisha
UNION ALL
SELECT 'Заказы', COUNT(*) FROM orders
UNION ALL
SELECT 'Избранные афиши', COUNT(*) FROM favorite_afishes
UNION ALL
SELECT 'Избранные места', COUNT(*) FROM favorite_places;

-- Показать пользователей с их ролями
SELECT id, first_name, last_name, email, role, cash FROM user ORDER BY id;

-- Показать афиши с их категориями
SELECT id, name, category, price, status, event_date_time_from FROM afisha ORDER BY event_date_time_from;

-- Показать места с их категориями
SELECT id, name, category, price_from, price_to, status FROM place ORDER BY name;

-- Показать заказы с информацией о пользователях и афишах
SELECT o.id, u.first_name, u.last_name, a.name as afisha_name, o.count, o.status 
FROM orders o 
JOIN user u ON o.user_id = u.id 
JOIN afisha a ON o.afisha_id = a.id 
ORDER BY o.id;

